#include <stdio.h>
#include <string.h>

// 前向声明
void ESP32_COMM_ProcessCommand(const char* cmd);
extern ringbuffer_t usart_rb;  // GPS环形缓冲区
extern uint8_t uart_rx_dma_buffer[1000];  // USART2 GPS DMA缓冲区

/**
 * @brief  UART空闲中断+DMA接收回调函数
 * @note   当接收完成或检测到空闲线路时触发
 * @param  huart: UART句柄
 * @param  Size: 接收到的数据长度
 * @retval None
 */
void HAL_UARTEx_RxEventCallback(UART_HandleTypeDef *huart, uint16_t Size)
{
    // ===== USART2 - GPS数据处理 =====
    if (huart == &huart2)
    {
        if (Size == 0) {
            return;
        }

        /* 将 DMA 缓冲区中的数据写入环形缓冲区 */
        ringbuffer_write(&usart_rb, uart_rx_dma_buffer, Size);

        /* 清空 DMA 缓冲区 */
        memset(uart_rx_dma_buffer, 0, sizeof(uart_rx_dma_buffer));
    }

    // ===== USART3 - ESP32通信处理 =====
    else if (huart == &huart3)
    {
        // 调试：打印接收状态
        printf("[USART3] 收到数据，长度=%d\r\n", Size);

        // 使用临时缓冲区保存接收数据（避免DMA覆盖）
        static char temp_buffer[256];
        memcpy(temp_buffer, uart3_rx_buffer, Size);
        temp_buffer[Size] = '\0';

        // 打印原始数据（十六进制+字符）
        printf("[USART3] 原始数据: ");
        for(int i = 0; i < Size; i++) {
            printf("%02X ", uart3_rx_buffer[i]);
        }
        printf("\r\n[USART3] 字符串: %s\r\n", temp_buffer);

        // 处理临时缓冲区中的命令（先处理，避免后续可能的重启影响）
        ESP32_COMM_ProcessCommand(temp_buffer);

        // 清空DMA缓冲区并重新启动接收
        memset(uart3_rx_buffer, 0, sizeof(uart3_rx_buffer));

        // 不管当前状态，直接强制停止并重启（避免HAL_BUSY问题）
        HAL_UART_DMAStop(&huart3);  // 停止DMA传输

        // 重新启动DMA接收
        HAL_StatusTypeDef dma_status = HAL_UARTEx_ReceiveToIdle_DMA(&huart3, uart3_rx_buffer, sizeof(uart3_rx_buffer));
        __HAL_DMA_DISABLE_IT(&hdma_usart3_rx, DMA_IT_HT);

        if (dma_status == HAL_OK) {
            printf("[USART3] ✓ DMA重启成功\r\n");
        } else {
            printf("[USART3] ✗ DMA重启失败! Status=%d\r\n", dma_status);
        }
    }
}

